# Removendo Itens de Qualquer Posição em uma Lista
clientes= ['Ana', 'Maria','Clara', 'Lucia']
print(clientes)

clientes.remove('Clara')
print(clientes)


#Removendo um Item pelo Valor
cliente_desmarcou = 'Ana'
clientes.remove(cliente_desmarcou)
print(clientes)
